L=[1,2,3,4,5]

def is_empty(L):
    if len(L)==0:
        return True
    else:
        return False
print(is_empty(L))



def first(L):
    return L[0]
print(first(L))




def last(L):
    return L[-1]
print(last(L))




def init(L):
    return L[:-1]
print(init(L))




def last(L):
    return L[1:]
print(last(L))


